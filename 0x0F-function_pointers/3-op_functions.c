#include "3-calc.h"
/**
 * op_add - Adds two numbers and return the result
 * @a: first number
 * @b: second number
 * Return: the sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Substract two numbers and return the result
 * @a: first number
 * @b: second number
 * Return: the substraction of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiplies two numbers and return the result
 * @a: first number
 * @b: second number
 * Return: the Multipliction of a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides two numbers and return the result
 * @a: first number
 * @b: second number
 * Return: the division of a and b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return the remainder of an integer division
 * @a: first number
 * @b: second number
 * Return: the remainder of (a % b)
*/
int op_mod(int a, int b)
{
	return (a % b);
}
